package inferencia.examples;

public class E5_posicion {
	int X;
	int Y;

	public E5_posicion(int X, int Y) {
		this.X=X;
		this.Y=Y;
	}

	public int getX() {
		return X;
	}

	public void setX(int x) {
		X = x;
	}

	public int getY() {
		return Y;
	}

	public void setY(int y) {
		Y = y;
	}

	
	
	
	
	
	
	
}
